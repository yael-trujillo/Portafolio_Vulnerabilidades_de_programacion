
public class numeroEnterosFlotantes {

    public static void main(String args[]) {
        /*
            Tipos primitivos enteros
            Byte = 8 bits
            short = 16 bits
            int = 32 bits
            long = 64 bits
        
            Almacenan valores positivos y negativos
         */

        System.out.println("##########NUMEROS ENTEROS#########");
        System.out.println("Valor minimo de byte: " + Byte.MIN_VALUE);
        System.out.println("Valor maximo de byte: " + Byte.MAX_VALUE);

        System.out.println("\nValor minimo de short: " + Short.MIN_VALUE);
        System.out.println("Valor maximo de short: " + Short.MAX_VALUE);

        System.out.println("\nValor minimo de int: " + Integer.MIN_VALUE);
        System.out.println("Valor maximo de int: " + Integer.MAX_VALUE);

        System.out.println("\nValor minimo de long: " + Long.MIN_VALUE);
        System.out.println("Valor maximo de long: " + Long.MAX_VALUE);

        /*
            Tipos primitivos flotantes
            float = 32 bits
            double = 64 bits
        
            Almacenan valores positivos y negativos
         */
        
        System.out.println("\n##########NUMEROS DECIMALES##########");

        System.out.println("\nValor minimo de float: " + Float.MIN_VALUE);
        System.out.println("Valor maximo de float: " + Float.MAX_VALUE);

        System.out.println("\nValor minimo de double: " + Double.MIN_VALUE);
        System.out.println("Valor maximo de double: " + Double.MAX_VALUE);
    }
}
